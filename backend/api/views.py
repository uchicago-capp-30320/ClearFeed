from django.http import (
    JsonResponse,
)  # converts python dict into proper JSON HTTP response that the extension can read.
from django.views.decorators.csrf import csrf_exempt
from api.models import (
    AppUser,
    BrowseSession,
    PoliticalLeaningResult,
    SentimentResult,
    TopicResult,
    Tweet,
    TweetMedia,
    ToxicityResult,
    TwitterAuthor,
    ViewedTweet,
)
from api.services.political_leaning import analyze_political_leaning_text
from api.services.sentiment import analyze_sentiment_text
from api.services.topic import analyze_topic_text
from api.services.toxicity import analyze_toxicity_text
import json
from datetime import datetime, timezone as dt_timezone
from django.utils import timezone

# temporary test users — replace with real auth later
# hardcoding multiple UUIDs so we can test session-specific and feed-level analysis
# across different simulated users before auth is implemented
TEST_USERS = {
    "grace": "a1b2c3d4-e5f6-7890-abcd-ef1234567890",
    "teddy": "b2c3d4e5-f6a7-8901-bcde-f12345678901",
    "yuri": "c3d4e5f6-a7b8-9012-cdef-123456789012",
}

# swap this to test as different users
HARDCODED_USER_ID = TEST_USERS["yuri"]

# csrf_exempt is a decorator that wraps the function and disables CSRF protection
# Request is the incoming HTTP request from the extension containing all the data — headers, body, method etc.


# CSRF stands for Cross-Site Request Forgery. It's a type of attack where a malicious website tricks your browser into making a request to another site you're logged into.
# Django protects against this by requiring every POST request to include a special secret token that only legitimate pages generated by Django would know.
# But our extension is not a Django-generated page. It's a browser extension running completely separately. It has no way to get that CSRF token from Django first.
# @csrf_exempt tells Django "skip the CSRF check for this specific endpoint."
@csrf_exempt
def import_dataset(request):
    # reads incoming data
    platform = request.headers.get("X-Zeeschuimer-Platform", "unknown")
    user_agent = request.headers.get("User-Agent", None)
    body = request.body.decode("utf-8")
    lines = [line.strip() for line in body.strip().split("\n") if line.strip()]

    # parse the NDJSON into JSON
    # json.loads converts a JSON string into a Python dictionary.
    # If a line is malformed the except json.JSONDecodeError catches the error and skips that line rather than crashing the whole request.
    # If no posts parsed successfully it returns a 400 error immediately.
    posts = []
    for line in lines:
        try:
            posts.append(json.loads(line))
        except json.JSONDecodeError:
            continue

    if not posts:
        return JsonResponse({"error": "no valid posts received"}, status=400)

    # ------------------------------------------------------------------------------
    # Step 1: Upsert app_user
    # get_or_create checks if this user already exists
    # if yes: returns existing user, does nothing
    # if no: creates new user with the hardcoded UUID
    # when auth is added, HARDCODED_USER_ID becomes the real user ID from the token

    app_user, _ = AppUser.objects.get_or_create(id=HARDCODED_USER_ID)

    # ------------------------------------------------------------------------------
    # Step 2: create browse_session input
    # Every upload is associated with a new session record
    # records platform, user_agent, and starts with status 'ingesting'
    session = BrowseSession.objects.create(
        user=app_user, platform=platform, user_agent=user_agent, status="ingesting"
    )

    # ----------------------------------------------------------------------------
    # Step 3: upsert twitter_authors
    # authors must be created before tweets because tweets reference them via FK
    # two cases:
    #   if author exists: update mutable fields to latest known values
    #     (screen_name, display_name, bio, location, is_blue_verified,
    #      followers_count, following_count, statuses_count)
    #     last_updated_at updates automatically via auto_now=True on the model
    #   if new author: create full record including immutable account_created_at
    # null filtering ensures we never overwrite good data with nulls from
    # records where Twitter returns incomplete user objects

    for post in posts:
        user = (
            post.get("data", {})
            .get("core", {})
            .get("user_results", {})
            .get("result", {})
        )

        raw_result = (
            post.get("data", {}).get("core", {}).get("user_results", {}).get("result")
        )
        tweet_id = post.get("data", {}).get("legacy", {}).get("id_str", "unknown")

        if not user:
            print(
                f"SKIPPED tweet {tweet_id} — result type: {type(raw_result)} — keys: {list(raw_result.keys()) if raw_result else 'None'}"
            )
            continue

        author_twitter_id = user.get("rest_id")
        if not author_twitter_id:
            print(f"SKIPPED tweet {tweet_id} — no rest_id found")
            continue

        # parse account_created_at from Twitter's date format into datetime
        # Twitter format: "Fri Jul 22 16:50:20 +0000 2011"
        account_created_at = None
        raw_account_created_at = user.get("core", {}).get("created_at")
        if raw_account_created_at:
            try:
                account_created_at = datetime.strptime(
                    raw_account_created_at, "%a %b %d %H:%M:%S +0000 %Y"
                ).replace(tzinfo=dt_timezone.utc)
            except ValueError:
                account_created_at = None

        # build mutable fields dict then filter out null values
        # this prevents overwriting previously good data with nulls
        # when Twitter returns incomplete user objects for some records
        mutable_fields = {
            "screen_name": user.get("core", {}).get(
                "screen_name"
            ),  # data.core.user_results.result.core.screen_name
            "display_name": user.get("core", {}).get(
                "name"
            ),  # data.core.user_results.result.core.name
            "bio": user.get("legacy", {}).get(
                "description"
            ),  # data.core.user_results.result.legacy.description
            "location": user.get("location", {}).get(
                "location"
            ),  # data.core.user_results.result.location.location
            "is_blue_verified": user.get(
                "is_blue_verified", False
            ),  # data.core.user_results.result.is_blue_verified
            "followers_count": user.get("legacy", {}).get(
                "followers_count"
            ),  # data.core.user_results.result.legacy.followers_count
            "following_count": user.get("legacy", {}).get(
                "friends_count"
            ),  # data.core.user_results.result.legacy.friends_count
            "statuses_count": user.get("legacy", {}).get(
                "statuses_count"
            ),  # data.core.user_results.result.legacy.statuses_count
        }

        # remove any keys where value is None so we never overwrite
        # previously stored good data with nulls
        mutable_fields = {k: v for k, v in mutable_fields.items() if v is not None}

        try:
            # existing author — update mutable fields only
            # account_created_at is immutable so we never touch it on update
            author = TwitterAuthor.objects.get(author_twitter_id=author_twitter_id)
            for field, value in mutable_fields.items():
                setattr(author, field, value)
            author.save()
        except TwitterAuthor.DoesNotExist:
            # new author — create full record including immutable account_created_at
            TwitterAuthor.objects.create(
                author_twitter_id=author_twitter_id,
                account_created_at=account_created_at,
                **mutable_fields,
            )
    # ----------------------------------------------------------------------------
    # Step 4: Insert tweets
    # each tweet is stored once globally
    # if the tweet already exists: skip (get_or_create does this)
    # if the tweet is new: insert with analysis_status = 'pending' so the analysis
    # pipeline knows to process it

    for post in posts:
        legacy = post.get("data", {}).get("legacy", {})
        user = (
            post.get("data", {})
            .get("core", {})
            .get("user_results", {})
            .get("result", {})
        )

        tweet_id = legacy.get("id_str")
        if not tweet_id:
            continue  # FOR NOW - DOES THIS WORK ?

        # extract hashtags from entities
        # Hashtags and mentioned users are stored as arrays in the NDJSON.
        # These list comprehensions extract the text values from each nested object
        entities = legacy.get("entities", {})
        hashtags = [h.get("text") for h in entities.get("hashtags", [])]

        # get the author object we just created
        author = None
        author_twitter_id = user.get("rest_id")
        if author_twitter_id:
            try:
                author = TwitterAuthor.objects.get(author_twitter_id=author_twitter_id)
            except TwitterAuthor.DoesNotExist:
                pass

        # parse tweet_created_at from Twitter's date format string into datetime
        tweet_created_at = None
        raw_created_at = legacy.get("created_at")
        if raw_created_at:
            try:
                tweet_created_at = datetime.strptime(
                    raw_created_at, "%a %b %d %H:%M:%S +0000 %Y"
                ).replace(tzinfo=dt_timezone.utc)
            except ValueError:
                tweet_created_at = None

        tweet, created = Tweet.objects.get_or_create(
            tweet_id=tweet_id,
            defaults={
                "author": author,
                "conversation_id": legacy.get("conversation_id_str"),
                "is_reply": bool(legacy.get("in_reply_to_status_id_str")),
                "in_reply_to_tweet_id": legacy.get("in_reply_to_status_id_str"),
                "in_reply_to_screen_name": legacy.get("in_reply_to_screen_name"),
                "timestamp_collected": post.get("timestamp_collected"),
                "full_text": legacy.get("full_text"),
                "hashtags": hashtags,
                "lang": legacy.get("lang"),
                "source_app": legacy.get("source"),
                "source_platform_url": post.get("source_platform_url"),
                "is_quote_status": legacy.get("is_quote_status", False),
                "is_retweet": bool(legacy.get("retweeted_status_result")),
                "possibly_sensitive": legacy.get("possibly_sensitive", False),
                "promoted": post.get("data", {}).get("promoted", False),
                "tweet_created_at": tweet_created_at,
                "analysis_status": "pending",
            },
        )

        # Analyze the tweet text during upload.
        if tweet.full_text:
            tweet.analysis_status = "processing"
            tweet.save(update_fields=["analysis_status"])

            result = analyze_sentiment_text(tweet.full_text)

            SentimentResult.objects.update_or_create(
                tweet=tweet,
                defaults={
                    "sentiment": result["sentiment"],
                    "confidence": result["confidence"],
                },
            )

            toxicity_result = analyze_toxicity_text(tweet.full_text)

            ToxicityResult.objects.update_or_create(
                tweet=tweet,
                defaults={
                    "toxicity_label": toxicity_result["toxicity_label"],
                    "confidence": toxicity_result["confidence"],
                },
            )

            topic_result = analyze_topic_text(tweet.full_text)

            TopicResult.objects.update_or_create(
                tweet=tweet,
                defaults={
                    "topic": topic_result["topic"],
                    "confidence": topic_result["confidence"],
                },
            )

            political_leaning_result = analyze_political_leaning_text(tweet.full_text)

            PoliticalLeaningResult.objects.update_or_create(
                tweet=tweet,
                defaults={
                    "leaning": political_leaning_result["leaning"],
                    "confidence": political_leaning_result["confidence"],
                },
            )

            tweet.analysis_status = "complete"
            tweet.save(update_fields=["analysis_status"])

        # -----------------------------------------------------------------------------
        # Step 5 - insert tweet_media
        # media is stored once per media_key - the same image can appear across
        # multiple tweets but we only store it once
        # extended_entities is used instead of entities because it contains
        # the full media info including video variants

        media_items = (
            post.get("data", {})
            .get("legacy", {})
            .get("extended_entities", {})
            .get("media", [])
        )
        for media in media_items:
            media_key = media.get("media_key")
            if not media_key:
                continue

            TweetMedia.objects.get_or_create(
                media_key=media_key,
                defaults={
                    "tweet": tweet,
                    # photo / video / animated_gif
                    "type": media.get("type"),
                    # media_url_https — thumbnail for videos, full image for photos
                    "media_url": media.get("media_url_https"),
                    "width": media.get("original_info", {}).get("width"),
                    "height": media.get("original_info", {}).get("height"),
                    "duration_ms": media.get("video_info", {}).get("duration_millis"),
                    "video_variants": media.get("video_info", {}).get("variants"),
                },
            )

        # -------------------------------------------------------------------------
        # STEP 6 — always insert viewed_tweet
        # this is the user-specific record — always insert regardless of
        # whether the tweet is new or already existed in the database
        # captures this specific user seeing that specific tweet
        # in their specific session at that specific time
        # stores time-sensitive stats at the time of viewing, such as favorite_count
        # the same tweet can have multiple viewed_tweet records across users
        # and across sessions for the same user

        ViewedTweet.objects.create(
            user=app_user,
            session=session,
            tweet=tweet,
            # outer envelope timestamp — when zeeschuimer captured this post
            timestamp_collected=post.get("timestamp_collected"),
            # engagement stats at moment of viewing — stored here not on tweet
            # because these numbers change over time and we want the snapshot
            # of what the user actually saw
            favorite_count=legacy.get("favorite_count"),
            retweet_count=legacy.get("retweet_count"),
            reply_count=legacy.get("reply_count"),
            quote_count=legacy.get("quote_count"),
            bookmark_count=legacy.get("bookmark_count"),
            view_count=int(post.get("data", {}).get("views", {}).get("count", 0) or 0),
            # entire ndjson record stored as backup
            # if we realize we missed a field during ingestion, raw_data lets us
            # re-parse without re-collecting data from the extension
            raw_data=post,
        )

    # -----------------------------------------------------------------------------
    # STEP 7 — mark session complete
    # update session status now that all posts are saved
    # ended_at records when ingestion finished
    # when queue is added later, this becomes status='queued' and
    # triggers analyze_session.delay(str(session.id))

    session.status = "complete"
    session.ended_at = timezone.now()
    session.save()

    print(f"Saved {len(posts)} posts from {platform} to database")

    return JsonResponse(
        {
            "status": "success",
            "session_id": str(session.id),
            "posts_received": len(posts),
        }
    )
