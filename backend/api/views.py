from django.shortcuts import render
from django.http import JsonResponse # converts python dict into proper JSON HTTP response that the extension can read.
from django.views.decorators.csrf import csrf_exempt
from api.models import Session, TwitterAuthor, TwitterPost
import json

# csrf_exempt is a decorator that wraps the function and disables CSRF protection
# Request is the incoming HTTP request from the extension containing all the data — headers, body, method etc.

#CSRF stands for Cross-Site Request Forgery. It's a type of attack where a malicious website tricks your browser into making a request to another site you're logged into. 
# Django protects against this by requiring every POST request to include a special secret token that only legitimate pages generated by Django would know.
# But our extension is not a Django-generated page. It's a browser extension running completely separately. It has no way to get that CSRF token from Django first.
#@csrf_exempt tells Django "skip the CSRF check for this specific endpoint."
@csrf_exempt
def import_dataset(request):
    # reads incoming data
    platform = request.headers.get('X-Zeeschuimer-Platform', 'unknown')
    body = request.body.decode('utf-8')
    lines = [line.strip() for line in body.strip().split('\n') if line.strip()]

    # parse the NDJSON into JSON
    # json.loads converts a JSON string into a Python dictionary.
    # If a line is malformed the except json.JSONDecodeError catches the error and skips that line rather than crashing the whole request. 
    # If no posts parsed successfully it returns a 400 error immediately.
    posts = []
    for line in lines:
        try:
            posts.append(json.loads(line))
        except json.JSONDecodeError:
            continue
    
    if not posts:
        return JsonResponse({"error": "no valid posts received"}, status=400)

    # Create a session to group these posts together
    session = Session.objects.create(
        user_id='test-user',  # hardcoded for now, replaced when auth is added
        platform=platform,
        status='processing'
    )

    # Create authors first since posts reference them
    for post in posts:
        user = post.get('data', {}).get('core', {}).get('user_results', {}).get('result', {})
        if not user:
            continue # FOR NOW?
        
        twitter_user_id = user.get('rest_id')
        if not twitter_user_id:
            continue

        # get_or_create avoids duplicate authors across sessions (keep it unique!)
        # It first looks in the database for an author with that twitter_user_id. If found it returns the existing one without touching it. If not found it creates a new one using the fields in defaults.
        # IF WE CARE ABOUT AUTHOR FOLLOWER COUNTS, WE MAY ALWAYS WANT TO UPDATE!
        TwitterAuthor.objects.get_or_create(
            twitter_user_id = twitter_user_id,
            defaults={
                'screen_name': user.get('core', {}).get('screen_name'),
                'display_name': user.get('core', {}).get('name'),
                'bio': user.get('legacy', {}).get('description'),
                'location': user.get('location', {}).get('location'),
                'is_blue_verified': user.get('is_blue_verified', False),
                'followers_count': user.get('legacy', {}).get('followers_count'),
                'following_count': user.get('legacy', {}).get('friends_count'),
                'total_tweets': user.get('legacy', {}).get('statuses_count'),
                'account_created_at': user.get('core', {}).get('created_at'),
            }
        )
    for post in posts:
        legacy = post.get('data', {}).get('legacy', {})
        user = post.get('data', {}).get('core', {}).get('user_results', {}).get('result', {})

        tweet_id = legacy.get('id_str')
        if not tweet_id:
            continue # FOR NOW!!

        # extract hashtags and mentioned users from entities
        # Hashtags and mentioned users are stored as arrays in the NDJSON. These list comprehensions extract just the text values from each nested object — so [{"text": "AbolishICE", "indices": [39,50]}] becomes just ["AbolishICE"].
        entities = legacy.get('entities', {})
        hashtags = [h.get('text') for h in entities.get('hashtags', [])]
        mentioned_users = [m.get('screen_name') for m in entities.get('user_mentions', [])]

        # get the author object we just created
        author = None
        twitter_user_id = user.get('rest_id')
        if twitter_user_id:
            try:
                author = TwitterAuthor.objects.get(twitter_user_id=twitter_user_id)
            except TwitterAuthor.DoesNotExist:
                pass

        # use get_or_create to avoid duplicate tweets across sessions
        TwitterPost.objects.get_or_create(
            tweet_id=tweet_id,
            defaults={
                'session': session,
                'author': author,
                'item_id': str(post.get('item_id', '')),
                'nav_index': post.get('nav_index'),
                'source_platform_url': post.get('source_platform_url'),
                'timestamp_collected': post.get('timestamp_collected'),
                'conversation_id': legacy.get('conversation_id_str'),
                'is_reply': bool(legacy.get('in_reply_to_status_id_str')),
                'in_reply_to_tweet_id': legacy.get('in_reply_to_status_id_str'),
                'in_reply_to_screen_name': legacy.get('in_reply_to_screen_name'),
                'full_text': legacy.get('full_text'),
                'lang': legacy.get('lang'),
                'tweet_created_at': legacy.get('created_at'),
                'is_quote_status': legacy.get('is_quote_status', False),
                'possibly_sensitive': legacy.get('possibly_sensitive', False),
                'promoted': post.get('data', {}).get('promoted', False),
                'favorite_count': legacy.get('favorite_count'),
                'retweet_count': legacy.get('retweet_count'),
                'reply_count': legacy.get('reply_count'),
                'quote_count': legacy.get('quote_count'),
                'bookmark_count': legacy.get('bookmark_count'),
                'view_count': int(post.get('data', {}).get('views', {}).get('count', 0) or 0),
                'hashtags': hashtags,
                'mentioned_users': mentioned_users,
                'raw_data': post,
            }
        )

    # step 4 — mark session complete
    session.status = 'complete'
    session.save()

    print(f"Saved {len(posts)} posts from {platform} to database")

    return JsonResponse({"status": "success", "posts_received": len(posts)})
